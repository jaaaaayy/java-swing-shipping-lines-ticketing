    void populateDestinationComboBox() {
        List<Transaction> transactions = destinations();
        
        for (Transaction t : transactions) {
            destinationComboBox.addItem(t.getDestination());
        }
    }
    
    void populatePassengerTypeComboBox() {
        List<Transaction> passengerTypes = passengerTypes();
        
        for (Transaction t : passengerTypes) {
            passengerTypeComboBox.addItem(t.getPassengerType() + " - " + t.getPassengerTypePrice());
        }
    }
    
    void populateSeatTypeComboBox() {
        List<Transaction> seatTypes = seatTypes();
        
        for (Transaction t : seatTypes) {
            seatTypeComboBox.addItem(t.getSeatType() + " - " + t.getSeatTypePrice());
        }
    }
    
    void calculateTotal() {
        if (t.getPassengerTypePrice() > 0 && t.getSeatTypePrice() > 0 && t.getQuantity() > 0) {
            t.setTotal((t.getPassengerTypePrice() + t.getSeatTypePrice()) * t.getQuantity());
            total.setText("Total: " + t.getTotal());

            if (t.getAmount() >= t.getTotal()) {
                t.setChange(t.getAmount() - t.getTotal());
                change.setText("Change: " + t.getChange());
                jLabel6.setText("");
            } else {
                jLabel6.setText("Insuficient amount");
            }
        } else {
            total.setText("Total: ");
            change.setText("Change: ");
        }
    }

