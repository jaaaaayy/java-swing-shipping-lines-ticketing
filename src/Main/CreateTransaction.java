    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        TransactionActionImplementation tai = new TransactionActionImplementation();
        
        t.setCreated_at(Timestamp.valueOf(LocalDateTime.now()));
        if (t.getAmount() < t.getTotal()) {
            JOptionPane.showMessageDialog(this, "Insufficient Balance!");
            return;
        }
        if (tai.createTransaction(t)) {
            JOptionPane.showMessageDialog(this, "Created Successfully!");
            
            destination.setText("Destination: " + t.getDestination());
            passengerType.setText("Passenger Type: " + t.getPassengerType());
            passengerTypePrice.setText("passenger Type Price: " + t.getPassengerTypePrice());
            seatType.setText("Seat Type: " + t.getSeatType());
            seatTypePrice.setText("seat Type Price: " + t.getSeatTypePrice());
            quantityField.setText("No. of Tickets: " + t.getQuantity());
            totalField.setText("Total: " + t.getTotal());
            amountField.setText("Amount: " + t.getAmount());
            changeField.setText("Change: " + t.getChange());
            dateField.setText("Date: " + t.getCreated_at());
        } else {
            JOptionPane.showMessageDialog(this, "Creation Failed!");
        }
    }//GEN-LAST:event_createActionPerformed

